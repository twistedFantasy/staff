<template>
  <div class="absence-table-container">
    <v-dialog v-model="dialog" max-width="500px">
      <v-btn slot="activator" color="primary" dark class="mb-2">Create Absence</v-btn>
      <v-card>
        <v-card-title>
          <span class="headline">Create Absence</span>
        </v-card-title>

        <v-card-text>
          <v-container grid-list-md>
            <v-layout wrap>
              <v-flex xs12 sm6 d-flex>
                <v-select :items="absenceReasonOptions" label="Reason" v-model="editedItem.reason"></v-select>
              </v-flex>
              <v-flex xs12 sm6 md4>
                <v-text-field v-model="editedItem.start_date" label="Start Data"></v-text-field>
              </v-flex>
              <v-flex xs12 sm6 md4>
                <v-text-field v-model="editedItem.end_date" label="End Data"></v-text-field>
              </v-flex>
              <v-flex xs12 sm6 md4>
                <v-text-field v-model="editedItem.notes" label="Notes"></v-text-field>
              </v-flex>
            </v-layout>
          </v-container>
        </v-card-text>

        <v-card-actions>
          <v-spacer></v-spacer>
          <v-btn color="blue darken-1" flat @click="close">Cancel</v-btn>
          <v-btn color="blue darken-1" flat @click="save">Save</v-btn>
        </v-card-actions>
      </v-card>
    </v-dialog>
    <div class="absence-table">
      <v-toolbar flat color="white" class="table-header">
        <v-toolbar-title>My Absences</v-toolbar-title>
        <div class="right-block">
          <div class="filter-block">
            <v-select
              v-model="selectedTypeOfFilter"
              :items="typeOfFiltresOptions"
              menu-props="auto"
              label="Select type of filter"
              hide-details
              prepend-icon="map"
              single-line
            ></v-select>
            <v-select
              v-if="selectedTypeOfFilter === 'reason' || selectedTypeOfFilter === 'status'"
              v-model="selectedFilter"
              :items="getCurrentOptons()"
              menu-props="auto"
              label="Select filter"
              hide-details
              prepend-icon="map"
              single-line
            ></v-select>
            <v-text-field
              v-if="selectedTypeOfFilter === 'start_date' || selectedTypeOfFilter === 'end_date'"
              v-model="selectedDateFilterValue"
              append-icon="search"
              label="yyyy-mm-dd"
              single-line
              hide-details
            ></v-text-field>
          </div>
          <v-icon @click="clearFilter()">clear</v-icon>
        </div>
      </v-toolbar>
      <v-data-table :headers="headers" :items="absences" class="elevation-1">
        <template slot="items" slot-scope="props">
          <td>{{ props.item.reason }}</td>
          <td class="text-xs-left">{{ props.item.start_date }}</td>
          <td class="text-xs-left">{{ props.item.end_date }}</td>
          <td class="text-xs-left">{{ props.item.notes }}</td>
          <td class="justify-center layout px-0">
            <v-icon small class="mr-2" @click="editItem(props.item)">edit</v-icon>
            <v-icon small @click="deleteItem(props.item)">delete</v-icon>
          </td>
        </template>
      </v-data-table>
    </div>
  </div>
</template>

<script>
import * as absenceService from "../services/absence.service";
import * as config from "@/config.js";

export default {
  data: () => ({
    selectedDateFilterValue: "",
    selectedTypeOfFilter: "",
    selectedFilter: "",
    dialog: false,
    absenceReasonOptions: config.absenceReasonOptions,
    typeOfFiltresOptions: config.typeOfFiltresOptions,
    absenceStatusOptions: config.absenceStatusOptions,
    headers: [
      {
        text: "Reason",
        align: "left",
        sortable: true,
        value: "reason"
      },
      { text: "Start Data", value: "start_date" },
      { text: "End Data", value: "end_date" },
      { text: "Notes", value: "notes" },
      { text: "Actions", value: "name", sortable: false }
    ],
    absences: [],
    editedIndex: -1,
    editedItem: {
      reason: "",
      start_date: 0,
      end_date: 0,
      notes: ""
    },
    defaultItem: {
      reason: "",
      start_date: 0,
      end_date: 0,
      notes: ""
    }
  }),

  watch: {
    dialog(val) {
      val || this.close();
    },
    selectedFilter(val) {
      if (val) {
        this.getAbsences({ type: this.selectedTypeOfFilter, value: val });
      }
    },
    selectedDateFilterValue(val) {
      if (val) {
        const re = /^\d{4}\-(0?[1-9]|1[012])\-(0?[1-9]|[12][0-9]|3[01])$/g;
        const isValid = re.test(val);
        if (isValid) {
          this.getAbsences({ type: this.selectedTypeOfFilter, value: val });
        }
      }
    }
  },

  created() {
    this.getAbsences();
  },

  methods: {
    getAbsences(dataFilter) {
      absenceService.getAllAbsencesByUserId(dataFilter).then(
        data => {
          this.$store.dispatch("absence/setAllAbsence", data.results);
          this.absences = data.results;
        },
        error => {
          console.log(error, "error");
        }
      );
    },

    clearFilter() {
      this.selectedDateFilterValue = "";
      this.selectedTypeOfFilter = "";
      this.selectedFilter = "";
      this.getAbsences();
    },

    getCurrentOptons() {
      const currentSelections = this.selectedTypeOfFilter;
      let answer = [];
      switch (currentSelections) {
        case "reason":
          answer = this.absenceReasonOptions;
          break;
        case "status":
          answer = this.absenceStatusOptions;
          break;
      }
      return answer;
    },

    editItem(item) {
      this.editedIndex = this.absences.indexOf(item);
      this.editedItem = Object.assign({}, item);
      this.dialog = true;
    },

    deleteItem(item) {
      absenceService.deleteAbsence(item.id).then(
        () => {
          this.getAbsences();
        },
        error => {
          console.log(error, "error");
        }
      );
    },

    close() {
      this.dialog = false;
      setTimeout(() => {
        this.editedItem = Object.assign({}, this.defaultItem);
        this.editedIndex = -1;
      }, 300);
    },

    save() {
      const data = {
        reason: this.editedItem.reason,
        start_date: this.editedItem.start_date,
        end_date: this.editedItem.end_date,
        notes: this.editedItem.notes
      };
      if (this.editedIndex == -1) {
        absenceService.createNewAbsence(data).then(
          () => {
            this.getAbsences();
            this.close();
          },
          error => {
            console.log(error, "error");
          }
        );
      } else {
        absenceService.editAbsence(data, this.editedItem.id).then(
          () => {
            this.getAbsences();
            this.close();
          },
          error => {
            console.log(error, "error");
          }
        );
      }
    }
  }
};
</script>

